/// Breakpoints used for responsive layouts.
class AppBreakpoints {
  AppBreakpoints._();

  /// Smaller than mobile portrait.
  static const double xs = 0.0;

  /// Mobile portrait and larger.
  static const double sm = 576.0;

  /// Tablets and larger.
  static const double md = 768.0;

  /// Desktop monitors and larger.
  static const double lg = 992.0;

  /// Large desktop monitors.
  static const double xl = 1200.0;

  /// Extra large desktop monitors.
  static const double xxl = 1400.0;
}
