/// Standardized icon sizes to maintain consistency across the system.
class AppIconSizes {
  AppIconSizes._();

  /// Extra small: 12.0
  static const double xs = 12.0;

  /// Small: 16.0
  static const double sm = 16.0;

  /// Medium standard: 24.0
  static const double md = 24.0;

  /// Large: 32.0
  static const double lg = 32.0;

  /// Extra large: 48.0
  static const double xl = 48.0;

  /// Huge: 64.0
  static const double xxl = 64.0;
}
